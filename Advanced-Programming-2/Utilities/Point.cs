namespace Advanced_Programming_2.Utilities

{
    // Point class
    public class Point
    {
        double x, y;
        public Point(double x, double y)
        {
            this.x = x;
            this.y = y;
        }
        // Getters
        public double getX()
        {
            return x;
        }

        public double getY()
        {
            return y;
        }

    }
}
